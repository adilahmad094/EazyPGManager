package net.eazypg.eazypgmanager.Activities.FoodDetails;

public class WednesdayFoodDetails {

    public String breakfast, lunch, dinner;

    public WednesdayFoodDetails() {
        breakfast = "";
        lunch = "";
        dinner = "";
    }

    public WednesdayFoodDetails(String breakfast, String lunch, String dinner) {
        this.breakfast = breakfast;
        this.lunch = lunch;
        this.dinner = dinner;
    }
}
